#include <bits/stdc++.h>
using namespace std;

class MyStack {
public:
    queue<int>q;
    MyStack() {
        
    }
    // after inserting , to make that element at top , remove everyone 
    // from behind ony by one and push them one by one in front  
    void push(int x) {
        q.push(x);
        for(int i=0;i<q.size()-1;i++){
            q.push(q.front());
            q.pop();
        }
    }
    
    int pop() {
        int x=q.front();
        q.pop();
        return x;
    }
    
    int top() {
        return q.front();
    }
    
    bool empty() {
        return q.size()==0;
    }
};

